function reportComparison(portal, spec) {
  const portalName = portal === 'site' ? '原站点后台' : '原代理后台'
  const roleFields = portal === 'site'
    ? ['代理类型', '代理身份', '所属团队', '业务线路', '结算单元', '生效周期']
    : ['代理类型', '代理身份', '业务线路', '结算单元', '生效周期']
  const roleFilters = portal === 'site'
    ? ['关键词', '代理类型', '代理身份', '业务线路', '生效周期', '状态']
    : ['关键词', '代理类型', '代理身份', '业务线路', '生效周期', '状态']
  const additions = {
    fields: [...roleFields, ...(spec.fields || [])].filter((value, index, values) => values.indexOf(value) === index),
    types: spec.types,
    filters: roleFilters,
    views: spec.views,
    actions: reportActions,
    rules: [
      ...(spec.rules || []),
      portal === 'site' ? '站点后台固定处理旺财体育本站数据，不展示所属站点筛选和列表字段' : '代理后台不展示所属站点和其他团队字段，主管主线、副线负责人和独立代理按当前身份范围查看数据',
      '金额类记录统一展示当月结余口径，历史已完成周期不回写',
    ],
  }
  return changed(`${portalName} / ${spec.title}`, spec.legacy, additions)
}

Object.entries(reportSpecs).forEach(([page, spec]) => {
  NOTE_COMPARISONS[`site:${page}`] = reportComparison('site', spec)
  if (page !== 'siteProfit') NOTE_COMPARISONS[`agent:${page}`] = reportComparison('agent', spec)
})

