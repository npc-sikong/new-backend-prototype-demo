function reportComparison(portal, spec) {
  const portalName = portal === 'site' ? '原站点后台' : '原代理后台'
  const additions = {
    fields: [...contextFields, ...(spec.fields || [])].filter((value, index, values) => values.indexOf(value) === index),
    types: spec.types,
    filters: reportFilters,
    views: spec.views,
    actions: reportActions,
    rules: [
      ...(spec.rules || []),
      portal === 'site' ? '站点后台只处理本站代理业务范围' : '主管主线、副线负责人和独立线主按各自身份范围查看数据',
      '金额类记录统一展示当月结余口径，历史已完成周期不回写',
    ],
  }
  return changed(`${portalName} / ${spec.title}`, spec.legacy, additions)
}

Object.entries(reportSpecs).forEach(([page, spec]) => {
  NOTE_COMPARISONS[`site:${page}`] = reportComparison('site', spec)
  if (page !== 'siteProfit') NOTE_COMPARISONS[`agent:${page}`] = reportComparison('agent', spec)
})
